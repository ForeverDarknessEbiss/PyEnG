class КартаПроекта:
    def __init__(self, структура, ширина=1600, высота=900):
        self.структура = структура
        self.файлы = структура["файлы"]
        self.связи = структура["связи"]
        self.вес_связей = структура.get("вес_связей", {})
        
        self.ширина = ширина
        self.высота = высота
        
        # Layout
        self.layout = ДревовидныйLayout(структура, ширина, высота)
        self.позиции = self.layout.позиции
        self.радиус_узла = self.layout.радиус_узла
        
        # Камера
        self.масштаб = 1.0
        self.сдвиг_x = 0
        self.сдвиг_y = 0
        
        # Управление
        self.нажата_мышь = False
        self.последняя_x = 0
        self.последняя_y = 0
        self.выбранный_узел = None
        
        # Цвета
        self.цвет_фона = (25, 25, 35)
        self.цвет_текста = (240, 240, 240)
        self.цвет_ребра_обычный = (80, 120, 200, 180)
        
        pygame.init()
        self.шрифт = pygame.font.SysFont("arial", 12)
        self.большой_шрифт = pygame.font.SysFont("arial", 16)
    
    def _получить_цвет_узла(self, файл):
        вес = self.вес_связей.get(файл, 0)
        норма = min(вес / 10, 1.0)
        if self.выбранный_узел == файл:
            return (255, 200, 50)
        красный = int(255 * норма)
        зелёный = int(255 * (1 - норма))
        return (красный, зелёный, 100)
    
    def _получить_цвет_ребра(self, от_файла, к_файлу):
        if self.выбранный_узел:
            if от_файла == self.выбранный_узел or к_файлу == self.выбранный_узел:
                вес = self.вес_связей.get(к_файлу, 0)
                норма = min(вес / 10, 1.0)
                красный = int(255 * норма)
                зелёный = int(255 * (1 - норма))
                return (красный, зелёный, 50, 220)
        return self.цвет_ребра_обычный
    
    def _преобразовать_экранные(self, x, y):
        return (x * self.масштаб + self.сдвиг_x, y * self.масштаб + self.сдвиг_y)
    
    def _преобразовать_мировые(self, экран_x, экран_y):
        return ((экран_x - self.сдвиг_x) / self.масштаб, (экран_y - self.сдвиг_y) / self.масштаб)
    
    def _найти_узел_под_курсором(self, курсор_x, курсор_y):
        мир_x, мир_y = self._преобразовать_мировые(курсор_x, курсор_y)
        for файл, (x, y) in self.позиции.items():
            if math.hypot(мир_x - x, мир_y - y) < self.радиус_узла:
                return файл
        return None
    
    def обработать_события(self):
        for событие in pygame.event.get():
            if событие.type == pygame.QUIT:
                return False
            
            elif событие.type == pygame.KEYDOWN:
                if событие.key == pygame.K_ESCAPE:
                    return False
                elif событие.key == pygame.K_s:  # <-- ДОБАВИТЬ ЭТОТ БЛОК
                    путь = self.экспортировать_связи()
                    # Временное сообщение (опционально)
                    print(f"Сохранено в {путь}")
            
            elif событие.type == pygame.MOUSEBUTTONDOWN:
                if событие.button == 1:
                    self.нажата_мышь = True
                    self.последняя_x, self.последняя_y = событие.pos
                    узел = self._найти_узел_под_курсором(событие.pos[0], событие.pos[1])
                    self.выбранный_узел = узел if узел else None
                
                elif событие.button == 4:  # Вверх
                    мышь_x, мышь_y = событие.pos
                    мир_x, мир_y = self._преобразовать_мировые(мышь_x, мышь_y)
                    self.масштаб *= 1.1
                    self.масштаб = min(self.масштаб, 3.0)
                    нов_x, нов_y = self._преобразовать_экранные(мир_x, мир_y)
                    self.сдвиг_x += мышь_x - нов_x
                    self.сдвиг_y += мышь_y - нов_y
                
                elif событие.button == 5:  # Вниз
                    мышь_x, мышь_y = событие.pos
                    мир_x, мир_y = self._преобразовать_мировые(мышь_x, мышь_y)
                    self.масштаб /= 1.1
                    self.масштаб = max(self.масштаб, 0.3)
                    нов_x, нов_y = self._преобразовать_экранные(мир_x, мир_y)
                    self.сдвиг_x += мышь_x - нов_x
                    self.сдвиг_y += мышь_y - нов_y
            
            elif событие.type == pygame.MOUSEBUTTONUP:
                if событие.button == 1:
                    self.нажата_мышь = False
            
            elif событие.type == pygame.MOUSEMOTION:
                if self.нажата_мышь:
                    dx = событие.pos[0] - self.последняя_x
                    dy = событие.pos[1] - self.последняя_y
                    self.сдвиг_x += dx
                    self.сдвиг_y += dy
                    self.последняя_x, self.последняя_y = событие.pos
        
        return True
    
    def рисовать(self, экран):
        экран.fill(self.цвет_фона)
        
        # Поверхность для полупрозрачных линий
        линия_поверхность = pygame.Surface((self.ширина, self.высота), pygame.SRCALPHA)
        
        # Рисуем рёбра
        for от_файла, к_файлам in self.связи.items():
            if от_файла not in self.позиции:
                continue
            от_поз = self.позиции[от_файла]
            от_экран = self._преобразовать_экранные(от_поз[0], от_поз[1])
            
            for к_файлу in к_файлам:
                if к_файлу not in self.позиции:
                    continue
                к_поз = self.позиции[к_файлу]
                к_экран = self._преобразовать_экранные(к_поз[0], к_поз[1])
                
                цвет = self._получить_цвет_ребра(от_файла, к_файлу)
                if len(цвет) == 4:
                    pygame.draw.line(линия_поверхность, цвет[:3], от_экран, к_экран, 2)
                else:
                    pygame.draw.line(экран, цвет, от_экран, к_экран, 2)
        
        экран.blit(линия_поверхность, (0, 0))
        
        # Рисуем узлы
        for файл, поз in self.позиции.items():
            экран_поз = self._преобразовать_экранные(поз[0], поз[1])
            цвет = self._получить_цвет_узла(файл)
            радиус = int(self.радиус_узла * self.масштаб)
            
            pygame.draw.circle(экран, цвет, (int(экран_поз[0]), int(экран_поз[1])), радиус)
            pygame.draw.circle(экран, self.цвет_текста, (int(экран_поз[0]), int(экран_поз[1])), радиус, 1)
            
            # Подготовка текста
            имя = Path(файл).stem
            if len(имя) > 15:
                имя = имя[:12] + "..."
            текст = self.шрифт.render(имя, True, self.цвет_текста)
            текст_rect = текст.get_rect(center=(экран_поз[0], экран_поз[1]))
            
            # === ФОН ПОД ТЕКСТОМ (чтобы не резало глаза) ===
            # Отступы вокруг текста
            отступ_x = 6
            отступ_y = 3
            # Прямоугольник фона
            фон_rect = текст_rect.inflate(отступ_x * 2, отступ_y * 2)
            # Полупрозрачный чёрный фон (0,0,0, альфа 180)
            фон_поверхность = pygame.Surface(фон_rect.size, pygame.SRCALPHA)
            фон_поверхность.fill((0, 0, 0, 180))
            экран.blit(фон_поверхность, фон_rect)
            
            # Рисуем текст поверх фона
            экран.blit(текст, текст_rect)
        

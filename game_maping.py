# 2. Древовидный layout с отталкиванием узлов
# ------------------------------------------------------------

class ДревовидныйLayout:
    def __init__(self, структура, ширина=1600, высота=900):
        self.структура = структура
        self.файлы = структура["файлы"]
        self.связи = структура["связи"]
        self.корневой = структура["корневой"]
        
        self.ширина = ширина
        self.высота = высота
        self.позиции = {}
        self.уровни = {}
        self.дети = defaultdict(list)
        
        self.радиус_узла = 20
        self.мин_расстояние = 55
        
        self._построить_дерево()
        self._расставить_позиции()
        self._развести_узлы()
    
    def _построить_дерево(self):
        if not self.корневой:
            return
        
        # Строим обратные связи: кто от кого зависит
        родители = defaultdict(list)
        for от_файла, к_файлам in self.связи.items():
            for к_файлу in к_файлам:
                родители[к_файлу].append(от_файла)
        
        # BFS от корня
        очередь = [self.корневой]
        посещено = {self.корневой}
        self.уровни[self.корневой] = 0
        
        while очередь:
            текущий = очередь.pop(0)
            for файл, родит in родители.items():
                if текущий in родит and файл not in посещено:
                    посещено.add(файл)
                    self.дети[текущий].append(файл)
                    self.уровни[файл] = self.уровни[текущий] + 1
                    очередь.append(файл)
        
        # Оставшиеся файлы (без связей) — на уровень 1, но не дальше
        for файл in self.файлы:
            if файл not in посещено:
                self.уровни[файл] = 1
                посещено.add(файл)
    
    def _расставить_позиции(self):
        if not self.корневой:
            return
        
        # Корень не в центре, а чуть выше (чтобы ветки уходили вниз)
        центр_x = self.ширина // 2
        центр_y = self.высота // 3  # было //2, теперь //3
        
        self.позиции[self.корневой] = (центр_x, центр_y)
        
        # Группируем по уровням
        уровни_группы = defaultdict(list)
        for файл, уровень in self.уровни.items():
            уровни_группы[уровень].append(файл)
        
        # Максимальный уровень, чтобы нормализовать радиусы
        макс_уровень = max(уровни_группы.keys()) if уровни_группы else 1
        
        for уровень, файлы in уровни_группы.items():
            if уровень == 0:
                continue
            
            # Радиус увеличивается с уровнем, но не улетает за экран
            # Максимальный радиус — половина меньшей стороны экрана минус отступ
            макс_радиус = min(self.ширина, self.высота) // 2 - 60
            # Нормализованный радиус: чем выше уровень, тем дальше
            доля = уровень / макс_уровень if макс_уровень > 0 else 1
            радиус = 70 + доля * (макс_радиус - 70)
            
            # Количество файлов на этом уровне
            кол_файлов = len(файлы)
            
            for i, файл in enumerate(файлы):
                # Пытаемся найти родителя, чтобы расположить ребёнка рядом с ним
                родитель = self._найти_родителя(файл)
                if родитель and родитель in self.позиции:
                    # Если есть родитель, считаем угол от него
                    рx, рy = self.позиции[родитель]
                    # Вектор от родителя к центру
                    dx = центр_x - рx
                    dy = центр_y - рy
                    базовый_угол = math.atan2(dy, dx)
                    # Смещение для разных детей
                    кол_детей = len(self.дети[родитель])
                    if кол_детей > 1:
                        шаг = (2 * math.pi) / кол_детей
                        угол = базовый_угол + шаг * i
                    else:
                        угол = базовый_угол
                else:
                    # Нет родителя — равномерно по кругу
                    угол = (2 * math.pi * i) / кол_файлов
                    # Добавляем сдвиг, чтобы не было идеального круга
                    угол += уровень * 0.3
                
                x = центр_x + радиус * math.cos(угол)
                y = центр_y + радиус * math.sin(угол)
                
                # Ограничиваем, чтобы не вылезало за края
                отступ = 60
                x = max(отступ, min(self.ширина - отступ, x))
                y = max(отступ, min(self.высота - отступ, y))
                
                self.позиции[файл] = (x, y)
    
    def _найти_родителя(self, файл):
        for родитель, дети in self.дети.items():
            if файл in дети:
                return родитель
        return None
    
    def _развести_узлы(self):
        # Несколько итераций отталкивания
        for _ in range(15):
            изменения = False
            for i, (файл1, поз1) in enumerate(self.позиции.items()):
                for файл2, поз2 in list(self.позиции.items())[i+1:]:
                    dx = поз1[0] - поз2[0]
                    dy = поз1[1] - поз2[1]
                    расстояние = math.hypot(dx, dy)
                    
                    if расстояние < self.мин_расстояние:
                        сила = (self.мин_расстояние - расстояние) * 0.4
                        if расстояние == 0:
                            угол = random.uniform(0, 2 * math.pi)
                        else:
                            угол = math.atan2(dy, dx)
                        
                        смещение_x = сила * math.cos(угол)
                        смещение_y = сила * math.sin(угол)
                        
                        новая_x1 = поз1[0] + смещение_x
                        новая_y1 = поз1[1] + смещение_y
                        новая_x2 = поз2[0] - смещение_x
                        новая_y2 = поз2[1] - смещение_y
                        
                        отступ = 40
                        self.позиции[файл1] = (
                            max(отступ, min(self.ширина - отступ, новая_x1)),
                            max(отступ, min(self.высота - отступ, новая_y1))
                        )
                        self.позиции[файл2] = (
                            max(отступ, min(self.ширина - отступ, новая_x2)),
                            max(отступ, min(self.высота - отступ, новая_y2))
                        )
                        изменения = True
            
            if not изменения:
                break


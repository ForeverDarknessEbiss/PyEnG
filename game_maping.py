# ------------------------------------------------------------
# 1. Сборщик структуры проекта
# ------------------------------------------------------------

class СборщикПроекта:
    def __init__(self, корневая_папка):
        self.корневая_папка = Path(корневая_папка).resolve()
        self.файлы = []
        self.связи = defaultdict(set)   # файл -> множество связанных файлов
        self.вес_связей = defaultdict(int)
        self.имена_файлов = {}          # имя_без_расширения -> полный_путь
        self.классы_в_файле = {}        # файл -> {имя_класса}
        self.наследования = []          # (ребёнок_файл, родитель_файл)
        
    def игнорировать_папку(self, имя):
        плохие_папки = {
            '__pycache__', 'venv', 'env', '.git', 'build', 'dist', 
            'logs', 'temp', 'node_modules', '.idea', '.vscode'
        }
        return имя in плохие_папки
    
    def найти_все_файлы(self):
        все_файлы = []
        for корень, папки, файлы in os.walk(self.корневая_папка):
            папки[:] = [п for п in папки if not self.игнорировать_папку(п)]
            for файл in файлы:
                if файл.endswith('.py'):
                    полный_путь = Path(корень) / файл
                    все_файлы.append(полный_путь)
        return все_файлы
    
    def извлечь_все_связи(self, путь_к_файлу):
        """Извлекает импорты, наследования, вызовы, создание объектов"""
        связи = set()
        
        try:
            with open(путь_к_файлу, 'r', encoding='utf-8') as f:
                дерево = ast.parse(f.read(), filename=str(путь_к_файлу))
            
            # 1. Обычные импорты
            for узел in ast.walk(дерево):
                if isinstance(узел, ast.Import):
                    for псевдоним in узел.names:
                        имя = псевдоним.name.split('.')[0]
                        связи.add(имя)
                elif isinstance(узел, ast.ImportFrom):
                    if узел.module:
                        имя = узел.module.split('.')[0]
                        связи.add(имя)
            
            # 2. Наследование классов (class Child(Parent):)
            for узел in ast.walk(дерево):
                if isinstance(узел, ast.ClassDef):
                    for база in узел.bases:
                        if isinstance(база, ast.Name):
                            имя_родителя = база.id
                            связи.add(имя_родителя)
                        elif isinstance(база, ast.Attribute):
                            # Родитель вида module.Class
                            связи.add(база.attr)
            
            # 3. Вызовы методов и обращение к атрибутам (объект.поле)
            for узел in ast.walk(дерево):
                if isinstance(узел, ast.Attribute):
                    # Ловим всё, что выглядит как объект.что_то
                    имя = узел.attr
                    связи.add(имя)
                elif isinstance(узел, ast.Call):
                    # Ловим создание объектов: ClassName()
                    if isinstance(узел.func, ast.Name):
                        связи.add(узел.func.id)
                    elif isinstance(узел.func, ast.Attribute):
                        связи.add(узел.func.attr)
            
            # 4. Добавляем имена классов, объявленных в этом файле
            классы = set()
            for узел in ast.walk(дерево):
                if isinstance(узел, ast.ClassDef):
                    классы.add(узел.name)
            if классы:
                self.классы_в_файле[str(путь_к_файлу)] = классы
                
        except Exception as e:
            pass
        
        return связи
    
    def построить_связи(self, все_файлы):
        # Строим словарь имён
        for путь in все_файлы:
            имя = путь.stem
            self.имена_файлов[имя] = str(путь)
        
        # Добавляем также имена классов как возможные цели
        for путь, классы in self.классы_в_файле.items():
            for класс in классы:
                self.имена_файлов[класс] = путь
        
        # Собираем связи для каждого файла
        for путь_файла in все_файлы:
            имена = self.извлечь_все_связи(путь_файла)
            for имя in имена:
                if имя in self.имена_файлов:
                    связанный = self.имена_файлов[имя]
                    self.связи[str(путь_файла)].add(связанный)
                    self.вес_связей[связанный] += 1
        
        return {k: list(v) for k, v in self.связи.items()}
    
    def найти_корневой_файл(self):
        # Ищем файл с if __name__ == "__main__"
        for файл in self.файлы:
            try:
                with open(файл, 'r', encoding='utf-8') as f:
                    if 'if __name__ == "__main__"' in f.read():
                        return str(файл)
            except:
                pass
        # Или файл main.py, game.py и т.д.
        for имя in ["main", "game", "main_game", "__init__"]:
            for файл in self.файлы:
                if файл.stem == имя:
                    return str(файл)
        return str(self.файлы[0]) if self.файлы else None
    
    def собрать(self):
        print(f"Сканируем: {self.корневая_папка}")
        self.файлы = self.найти_все_файлы()
        print(f"Найдено .py файлов: {len(self.файлы)}")
        
        связи = self.построить_связи(self.файлы)
        корневой = self.найти_корневой_файл()
        
        # Выводим статистику
        всего_связей = sum(len(v) for v in связи.values())
        print(f"Построено связей: {всего_связей}")
        
        return {
            "файлы": [str(ф) for ф in self.файлы],
            "связи": связи,
            "корневой": корневой,
            "вес_связей": dict(self.вес_связей)
        }

